function drawRangerFoot(p,q,color)
% drawRangerFoot(p,q,color)
%
% This function draws Ranger's foot using the patch command. 
%
% INPUTS:
%   p = [2, 1] = vector of ankle joint position. [x;y]
%   q = scalar = angle of the foot in absolute coordinates. 0 == -j axis
%   c = [1, 3] = color vector (eg. [1,0,0] = red)
%

% The points to use for the Ranger's foot 'patch'. Computed by
% MAIN_circleModel.m. Units are in meters, and the origin is at the ankle
% joint of Ranger.
pts = [...
  -0.026429428615028  -0.000000000000000;
  -0.026426648038926  -0.041992848534558;
  -0.026131692397091  -0.048661536042142;
  -0.024655769295647  -0.054632731880501;
  -0.021583857509120  -0.058214018380363;
  -0.017432031101172  -0.059241938928645;
   0.001266139070936  -0.059996632788895;
   0.020098157666312  -0.058988929075260;
   0.041114164920631  -0.055724984580118;
   0.066829204694759  -0.048508565082394;
   0.100008164943976  -0.033207791933129;
   0.104806417176947  -0.029833835662939;
   0.106714454992559  -0.025442484647122;
   0.104112450598096  -0.020106339285829;
   0.097161183037862  -0.014435969109948;
   0.087975252652517  -0.009202492105387;
   0.035985672638352   0.017992836319176;
   0.017756253877436   0.022839771254438;
   0.004969814161446   0.022812819781696;
  -0.005190543998239   0.021987497216742;
  -0.015200259655993   0.018868180499513;
  -0.023288120475901   0.011144053339006;
  -0.026429428615028   0.000000000000000];

% Rotation:
c = cos(q); s = sin(q);
R = [c, s;  -s, c];
pts = pts*R;

% Translation:
pts(:,1) = pts(:,1) + p(1);
pts(:,2) = pts(:,2) + p(2);

% Plot:
patch(pts(:,1),pts(:,2),color); axis equal;

end